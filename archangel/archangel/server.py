@app.route('/account/settings', methods=['GET', 'POST'])
def account_setting():
    if 'id' not in session: return abort(401)
    if request.method == 'POST':
        if 'value' in request.form and 'key' in request.form:
            key = request.form['key']
            value = request.form['value']
            key = str(key).lower()
