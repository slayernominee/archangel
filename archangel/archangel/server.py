@app.route('/redirect/stream/<id>', methods=['GET', 'POST'])
def redirect_stream(id):
    episode = stream.getEpisode(id)
    link = str(episode['link'])
    # Voe
    if 'voe.sx/' in link and '/v/' in link:
        link = link.replace('voe.sx/v/', 'voe.sx/e/')
    if 'voe.sx/' in link and '/e/' not in link:
        link = link.split('voe.sx/')[0] + 'voe.sx/e/' + link.split('voe.sx/')[1]
    # Vupload
    if 'vupload.com/v/' in link:
        link = link.replace('vupload.com/v/', 'vupload.com/e/')
    elif 'vupload.com/' in link and '/e/' not in link and '/v/' not in link:
        link = link.split('vupload.com/')[0] + 'vupload.com/e/' +  link.split('vupload.com/')[1]
    response = make_response(redirect(link))
    return response


